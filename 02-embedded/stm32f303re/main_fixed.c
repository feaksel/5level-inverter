  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_TIM8_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  // Send startup message via UART
  const char *msg = "\r\n=== STM32F303RE PWM Test ===\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 1000);
  msg = "Clock: 72MHz, PWM: 5kHz\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 1000);
  msg = "Starting PWM on TIM1 and TIM8...\r\n\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 1000);

  // Set 50% duty cycle for testing (Period = 14399, so 50% = 7200)
  uint16_t duty_50_percent = 7200;

  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, duty_50_percent);
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, duty_50_percent);
  __HAL_TIM_SET_COMPARE(&htim8, TIM_CHANNEL_1, duty_50_percent);
  __HAL_TIM_SET_COMPARE(&htim8, TIM_CHANNEL_2, duty_50_percent);

  // Start TIM1 PWM with complementary outputs
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);       // PA8  - TIM1_CH1
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);    // PB13 - TIM1_CH1N (complementary)
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);       // PA9  - TIM1_CH2
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);    // PB14 - TIM1_CH2N (complementary)

  // Start TIM8 PWM with complementary outputs
  HAL_TIM_PWM_Start(&htim8, TIM_CHANNEL_1);       // PC6  - TIM8_CH1
  HAL_TIMEx_PWMN_Start(&htim8, TIM_CHANNEL_1);    // PC10 - TIM8_CH1N (complementary)
  HAL_TIM_PWM_Start(&htim8, TIM_CHANNEL_2);       // PC7  - TIM8_CH2
  HAL_TIMEx_PWMN_Start(&htim8, TIM_CHANNEL_2);    // PC11 - TIM8_CH2N (complementary)

  // CRITICAL: Enable Main Output Enable (MOE) bit for advanced timers
  // Without this, complementary PWM outputs will NOT work!
  __HAL_TIM_MOE_ENABLE(&htim1);
  __HAL_TIM_MOE_ENABLE(&htim8);

  msg = "PWM Started!\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 1000);
  msg = "Check oscilloscope:\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 1000);
  msg = "  PA8  = TIM1_CH1  (50% @ 5kHz)\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 1000);
  msg = "  PB13 = TIM1_CH1N (inverted with dead-time)\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 1000);
  msg = "  PC6  = TIM8_CH1  (50% @ 5kHz)\r\n\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 1000);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

    // Blink LED to show we're alive
    HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
    HAL_Delay(500);
  }
